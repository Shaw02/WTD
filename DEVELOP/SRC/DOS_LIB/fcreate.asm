;---------------------------------------------------------------|
;		ファイルの作成					|
;---------------------------------------------------------------|
;	●引数							|
;		cFilename	ファイルネームのポインタ	|
;		iAttr		ファイル属性			|
;	●返り値						|
;		ax	ハンドル				|
;---------------------------------------------------------------|
.code
File_Create	proc	near	uses cx dx ds,
		cFilename:dword,
		iAttr:word

		lds	dx,cFilename
		mov	cx,iAttr
		mov	ah,3ch		;
		int	21h		;

		jnc	File_Create_Ok	;
		invoke	File_Err	;
File_Create_Ok:				;
		ret			;
File_Create	endp			;
;****************************************************************
	end
