include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		メモリ開放					|
;---------------------------------------------------------------|
;	●引数							|
;		CloseSegment	開放するメモリのセグメント	|
;	●返り値						|
;		無し						|
;---------------------------------------------------------------|
.code
Memory_Close	proc	near	uses ax es,
	CloseSegment:word

	mov	es,CloseSegment		;

	MOV	AH,49H			;
	INT	21H			;データ領域の開放

	JNC	Memory_Close_Err	;
	call	File_Err		;
Memory_Close_Err:			;

	RET				;
Memory_Close	endp			;
;****************************************************************
	end
