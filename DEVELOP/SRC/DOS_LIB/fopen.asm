;---------------------------------------------------------------|
;		ファイルのオープン				|
;---------------------------------------------------------------|
;	●引数							|
;		cFilename	ファイルネームのポインタ	|
;		cMode		0 ･･･ Read			|
;				1 ･･･ Write			|
;				2 ･･･ Randam			|
;	●返り値						|
;		ax	ハンドル				|
;---------------------------------------------------------------|
.code
File_Open	proc	near	uses cx dx ds,
		cFilename:dword,
		cMode:byte

		lds	dx,cFilename
		mov	al,cMode

		mov	ah,3dh		;
		int	21h		;

		jnc	File_Open_Ok	;
		invoke	File_Err	;
File_Open_Ok:				;
		ret			;
File_Open	endp			;
;****************************************************************
	end
