include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		ASCII CODE→１６進コード(-127〜255)		|
;---------------------------------------------------------------|
;	引数							|
;		DS:DX←変換したい文字列先頭アドレス		|
;	返り値							|
;		AH←変換後					|
;		DS:BX←次のアドレス				|
;---------------------------------------------------------------|
.code
ASC2HEX8	proc	near	uses cx
	push	ax
	invoke	ASC2HEX16
	mov	cl, al
	pop	ax
	mov	ah, cl
	RET				;
ASC2HEX8	endp			;
;****************************************************************
	end
